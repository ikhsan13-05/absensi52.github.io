export function formatTanggal(date = new Date()) {
  return new Intl.DateTimeFormat("id-ID", {
    day: "2-digit",
    month: "long",
    year: "numeric",
  }).format(date);
}

export function getHari(date = new Date()) {
  return new Intl.DateTimeFormat("id-ID", {
    weekday: "long",
  }).format(date);
}

